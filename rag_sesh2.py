# BM25 works on token overlap — tokenise by lowercasing + splitting
def tokenize(text):
    return re.sub(r'[^a-z0-9\s]', '', text.lower()).split()

tokenized_corpus = [tokenize(c["text"]) for c in chunks]
bm25 = BM25Okapi(tokenized_corpus)
print(f"BM25 index built over {len(chunks)} chunks ✓")

chunk_texts      = [c["text"] for c in chunks]
chunk_embeddings = model.encode(chunk_texts, convert_to_tensor=True)
print(f"Semantic embeddings shape: {chunk_embeddings.shape} ✓")


# ── Cell 6: Search functions ──────────────────────────────────
def bm25_search(query, top_k=2):
    tokens = tokenize(query)
    scores = bm25.get_scores(tokens)
    ranked = sorted(zip(scores, chunks), key=lambda x: x[0], reverse=True)
    return ranked[:top_k]

def semantic_search(query, top_k=2):
    q_emb  = model.encode(query, convert_to_tensor=True)
    scores = util.cos_sim(q_emb, chunk_embeddings)[0].tolist()
    ranked = sorted(zip(scores, chunks), key=lambda x: x[0], reverse=True)
    return ranked[:top_k]

